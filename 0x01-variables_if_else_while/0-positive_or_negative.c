#include <stdio.h>

/**
* main - Entry point of the program
*
* Check if number is positive or negative or zero
*
* Return: Always 0 (success)
*/
int main(void)
{
int n = 0;

if (n > 0)
{
printf("is positive\n");
}
else if (n == 0)
{
printf("is zero\n");
}
else
{
printf("is negative\n");
}
return (0);
}
